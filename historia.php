                    <p>Hace cincuenta años, un joven juninense de 23 años que estaba en relación de dependencia sintió la necesidad de ir por más; probar, independizarse. 
                        Soñó a lo grande y no le fue mal, para nada mal; como todo en la vida, con sus vaivenes. 
                        De todas maneras, a lo largo del camino, el balance es más que positivo.
                    </p>
                    <p>José recuerda, cuenta y comparte su andar: «es una historia medio rara. 
                        En aquella época, trabajaba en una casa de repuestos y pensaba en «qué faltaba en Junín, qué negocio no había»; me quería independizar. 
                        Tenía dos o tres posibilidades; una era la venta de repuestos para camiones. 
                        Luego surgió la comercialización de repuestos para tractor, porque no había 
                        ningún negocio que se dedicara a este rubro».
                    </p>
