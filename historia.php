                        Con lo que me vendió y lo que pude comprar empecé: ¡fue una aventura!. 
                        Más de una noche me he preguntado: «¿qué hago si no vendo estas cosas?». 
                        Había renunciado y me planteaba: «si me va mal, ¿a qué me dedico?».
                    </p>

