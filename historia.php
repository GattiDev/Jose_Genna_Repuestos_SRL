                        Nos sirvió como experiencia para decir: «estamos en esta situación y, de alguna forma, 
                        tenemos que reaccionar»; ahí salimos a la calle, a la zona a vender porque Junín y 
                        alrededores estaba muy complicado por la situación reinante. Empezamos a recorrer el sur, 
                        ciudades como Olavarría y tomamos otro horizonte para poder subsistir. 
                        Nos fue bastante bien, y hoy seguimos atendiendo a toda esa gente», concluyó.
